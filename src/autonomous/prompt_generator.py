"""
Prompt Pack Generator - Creates music and voiceover prompts from video analysis.

Milestone 4 implementation.

Reads candidates.json and generates:
- suno_prompt.md/.json - Music generation prompts for Suno AI
- voiceover_script.md/.json - Voiceover script templates

No API calls, no Resolve modifications. Human-in-the-loop by default.
"""

import json
import logging
from dataclasses import dataclass, field, asdict
from datetime import datetime
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple

logger = logging.getLogger("autonomous.prompt_generator")


# ============================================================================
# Data Classes
# ============================================================================

@dataclass
class SectionTiming:
    """Timing for a music section."""
    name: str  # hook, build, peak, outro
    start_sec: float
    end_sec: float
    duration_sec: float
    description: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return asdict(self)


@dataclass
class MusicPrompt:
    """Complete music generation prompt pack."""

    # Core prompt
    style: str = "cinematic orchestral"
    mood: str = "epic"
    energy_level: str = "medium"  # low, medium, high

    # Tempo
    bpm_min: int = 90
    bpm_max: int = 120

    # Duration
    duration_sec: float = 60.0

    # Sections
    sections: List[SectionTiming] = field(default_factory=list)

    # Raw prompt for Suno
    suno_prompt: str = ""
    suno_tags: List[str] = field(default_factory=list)

    # Source analysis reference
    analysis_summary: Dict[str, Any] = field(default_factory=dict)

    # Metadata
    generated_at: str = field(default_factory=lambda: datetime.now().isoformat())

    def to_dict(self) -> Dict[str, Any]:
        return {
            "style": self.style,
            "mood": self.mood,
            "energy_level": self.energy_level,
            "bpm_range": {"min": self.bpm_min, "max": self.bpm_max},
            "duration_sec": self.duration_sec,
            "sections": [s.to_dict() for s in self.sections],
            "suno_prompt": self.suno_prompt,
            "suno_tags": self.suno_tags,
            "analysis_summary": self.analysis_summary,
            "generated_at": self.generated_at,
        }

    def to_json(self, indent: int = 2) -> str:
        return json.dumps(self.to_dict(), indent=indent)

    def to_markdown(self) -> str:
        """Generate human-readable markdown prompt."""
        lines = [
            "# Suno Music Prompt",
            "",
            f"Generated: {self.generated_at}",
            "",
            "## Quick Copy Prompt",
            "",
            "```",
            self.suno_prompt,
            "```",
            "",
            "## Tags",
            "",
            ", ".join(self.suno_tags) if self.suno_tags else "_No tags_",
            "",
            "## Details",
            "",
            f"- **Style**: {self.style}",
            f"- **Mood**: {self.mood}",
            f"- **Energy**: {self.energy_level}",
            f"- **BPM Range**: {self.bpm_min}-{self.bpm_max}",
            f"- **Target Duration**: {self.duration_sec:.1f}s",
            "",
            "## Section Breakdown",
            "",
        ]

        if self.sections:
            lines.append("| Section | Time | Duration | Notes |")
            lines.append("|---------|------|----------|-------|")
            for section in self.sections:
                lines.append(
                    f"| {section.name.title()} | "
                    f"{section.start_sec:.1f}s - {section.end_sec:.1f}s | "
                    f"{section.duration_sec:.1f}s | "
                    f"{section.description} |"
                )
        else:
            lines.append("_No sections defined_")

        lines.extend([
            "",
            "## Source Analysis",
            "",
            f"- Total clips: {self.analysis_summary.get('total_clips', 'N/A')}",
            f"- Total duration: {self.analysis_summary.get('total_duration_sec', 'N/A')}s",
            f"- Scene count: {self.analysis_summary.get('total_scenes', 'N/A')}",
            f"- Average motion: {self.analysis_summary.get('average_motion_score', 'N/A')}",
            f"- Has audio: {not self.analysis_summary.get('clips_with_audio', 0) == 0}",
            "",
            "---",
            "_Generated by DaVinci Resolve MCP Autonomous Editor_",
        ])

        return "\n".join(lines)


@dataclass
class VoiceoverSection:
    """A section of voiceover script."""
    index: int
    timecode: str  # "00:05"
    start_sec: float
    duration_sec: float
    scene_description: str
    suggested_text: str
    tone: str = "neutral"

    def to_dict(self) -> Dict[str, Any]:
        return asdict(self)


@dataclass
class VoiceoverScript:
    """Complete voiceover script template."""

    # Tone settings
    voice_style: str = "professional narrator"
    pace: str = "measured"  # slow, measured, fast
    tone: str = "informative"  # informative, dramatic, conversational

    # Sections
    sections: List[VoiceoverSection] = field(default_factory=list)

    # Full script
    full_script: str = ""

    # ElevenLabs settings (user-configurable)
    elevenlabs_voice_id: Optional[str] = None  # User selects in ElevenLabs
    elevenlabs_stability: float = 0.5
    elevenlabs_similarity: float = 0.75

    # Source analysis reference
    analysis_summary: Dict[str, Any] = field(default_factory=dict)

    # Metadata
    generated_at: str = field(default_factory=lambda: datetime.now().isoformat())

    def to_dict(self) -> Dict[str, Any]:
        return {
            "voice_style": self.voice_style,
            "pace": self.pace,
            "tone": self.tone,
            "sections": [s.to_dict() for s in self.sections],
            "full_script": self.full_script,
            "elevenlabs_settings": {
                "voice_id": self.elevenlabs_voice_id,
                "stability": self.elevenlabs_stability,
                "similarity_boost": self.elevenlabs_similarity,
            },
            "analysis_summary": self.analysis_summary,
            "generated_at": self.generated_at,
        }

    def to_json(self, indent: int = 2) -> str:
        return json.dumps(self.to_dict(), indent=indent)

    def to_markdown(self) -> str:
        """Generate human-readable markdown script."""
        lines = [
            "# Voiceover Script",
            "",
            f"Generated: {self.generated_at}",
            "",
            "## Voice Settings",
            "",
            f"- **Style**: {self.voice_style}",
            f"- **Pace**: {self.pace}",
            f"- **Tone**: {self.tone}",
            "",
            "## Full Script",
            "",
            "```",
            self.full_script if self.full_script else "[Script content goes here]",
            "```",
            "",
            "## Section Breakdown",
            "",
        ]

        if self.sections:
            for section in self.sections:
                lines.extend([
                    f"### [{section.timecode}] Section {section.index + 1}",
                    "",
                    f"**Scene**: {section.scene_description}",
                    "",
                    f"**Suggested text** ({section.tone} tone, ~{section.duration_sec:.1f}s):",
                    "",
                    f"> {section.suggested_text}",
                    "",
                ])
        else:
            lines.append("_No sections defined - add your script content above_")

        lines.extend([
            "",
            "## ElevenLabs Settings",
            "",
            f"- Voice ID: `{self.elevenlabs_voice_id or '[Select your voice in ElevenLabs]'}`",
            f"- Stability: {self.elevenlabs_stability}",
            f"- Similarity Boost: {self.elevenlabs_similarity}",
            "",
            "## Usage Notes",
            "",
            "1. Select a voice in [ElevenLabs Voice Library](https://elevenlabs.io/voice-library)",
            "2. Copy the full script or individual sections",
            "3. Paste into ElevenLabs or your preferred TTS service",
            "4. Adjust timing to match your video edit",
            "",
            "---",
            "_Generated by DaVinci Resolve MCP Autonomous Editor_",
        ])

        return "\n".join(lines)


