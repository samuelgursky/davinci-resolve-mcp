def generate_prompt_packs(
    candidates_path: Path,
    output_dir: Optional[Path] = None,
    style_override: Optional[str] = None,
) -> Dict[str, Path]:
    """
    Convenience function to generate all prompt packs.

    Args:
        candidates_path: Path to candidates.json
        output_dir: Output directory (default: work/prompts)
        style_override: Optional music style override

    Returns:
        Dict mapping output type to file path
    """
    generator = PromptGenerator(
        work_dir=candidates_path.parent.parent if candidates_path else None
    )

    candidates = generator.load_candidates(candidates_path)

    music_prompt = generator.generate_music_prompt(candidates, style_override)
    voiceover_script = generator.generate_voiceover_script(candidates)

    return generator.save_prompts(music_prompt, voiceover_script, output_dir)
