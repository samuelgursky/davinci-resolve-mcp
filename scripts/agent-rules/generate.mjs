  kernelActions: grab('docs/kernels/README.md', /\*\*(\d+)\s*actions\*\*/, 'kernel action count'),
  advanced: grab('resolve-advanced/README.md', /##\s*Tools\s*\((\d+)\)/, 'advanced tool count'),
