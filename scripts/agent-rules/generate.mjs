// ── Emit helpers ───────────────────────────────────────────────────────────────
const results = [];
function emit(relPath, content) {
  const abs = join(ROOT, relPath);
  const want = content.endsWith('\n') ? content : content + '\n';
  const have = existsSync(abs) ? readFileSync(abs, 'utf8') : null;
  if (have === want) { results.push(['ok', relPath]); return; }
  if (CHECK) { results.push(['stale', relPath]); return; }
  mkdirSync(dirname(abs), { recursive: true });
  writeFileSync(abs, want);
  results.push([have === null ? 'created' : 'updated', relPath]);
}
function emitBlock(relPath, begin, end, block) {
  const abs = join(ROOT, relPath);
  const orig = existsSync(abs) ? readFileSync(abs, 'utf8') : '';
  const section = `${begin}\n${block}\n${end}`;
  let next;
  if (orig.includes(begin) && orig.includes(end)) {
    next = orig.replace(new RegExp(`${begin}[\\s\\S]*?${end}`), section);
  } else {
    next = (orig.trimEnd() + '\n\n' + section + '\n').replace(/^\n+/, '');
  }
  if (orig === next) { results.push(['ok', relPath]); return; }
  if (CHECK) { results.push(['stale', relPath]); return; }
  writeFileSync(abs, next);
  results.push([orig ? 'updated' : 'created', relPath]);
}

