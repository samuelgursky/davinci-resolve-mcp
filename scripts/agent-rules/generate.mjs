// ── Counts: parsed from their canonical sources (never hand-copied) ────────────
function read(p) {
  return readFileSync(join(ROOT, p), 'utf8');
}
function grab(p, re, label) {
  const m = read(p).match(re);
  if (!m) throw new Error(`generate.mjs: could not parse ${label} from ${p} (pattern ${re})`);
  return m[1];
}
