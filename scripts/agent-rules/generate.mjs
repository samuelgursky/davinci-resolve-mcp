// Single source of truth for cross-platform agent rule files.
//
// The *content* (AGENTS.md, docs/kernels/*, docs/guides/*, resolve-advanced/) is
// platform-neutral. Only the trigger/discovery layer differs per IDE. This script
// emits the thin per-platform shims from ONE manifest so they cannot drift, and
// parses tool/action counts straight from their canonical docs so those numbers
// cannot diverge from their own sources.
//
// Usage:
//   node scripts/agent-rules/generate.mjs           # write/update all outputs
//   node scripts/agent-rules/generate.mjs --check    # exit 1 if any output is stale
//
// The dynamic, every-client layer is the MCP server's per-domain prompts
// (color_grade_workflow / timeline_edit_workflow / conform_workflow /
// delivery_workflow in src/server.py); this script only covers the static,
// repo-file layer that non-MCP-prompt discovery relies on.

